// Test: do-while loop
public class DoWhileLoop {
    public static void main(String[] args) {
        int k = 0;
        do {
            System.out.print(k + " ");
            k++;
        } while (k < 3);
        System.out.println();

        System.out.println("=== Do-While Loop: PASS ===");
    }
}
